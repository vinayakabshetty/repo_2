package javaProgramsToPractise;

public class EncapsulationExample {
	private int a, b;
	
	public int getA() {
		System.out.println(a);
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		System.out.println(b);
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}
	
}